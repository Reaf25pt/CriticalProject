package dto;

public class Keyword {

    private int id;
    private String title;


    public Keyword() {
    }

    public Keyword(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
